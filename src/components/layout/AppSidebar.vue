<script setup>
import { ref, computed } from 'vue'
import { RouterLink, useRouter } from 'vue-router'
import { useAuthStore } from '../../stores/auth'

const authStore = useAuthStore()
const router = useRouter()

const isCollapsed = ref(false)
const pendingApprovals = ref(3)
const unreadMessages = ref(0)

const isHR = computed(() => authStore.isHR)
const isAdmin = computed(() => authStore.isAdmin)
const isSupervisor = computed(() => authStore.isSupervisor)
const isManager = computed(() => authStore.isManager)
const isEmployee = computed(() => authStore.isEmployee)
const isAuditor = computed(() => authStore.role === 'auditor' || authStore.role === 'admin')

const userInitials = computed(() => {
  const first = authStore.user?.firstName?.[0] || ''
  const last = authStore.user?.lastName?.[0] || ''
  return (first + last).toUpperCase() || 'U'
})

const roleLabel = computed(() => {
  const role = authStore.user?.role || 'employee'
  const labels = {
    admin: 'Administrator',
    hr: 'HR Manager',
    hr_admin: 'HR Admin',
    manager: 'Manager',
    supervisor: 'Supervisor',
    employee: 'Employee',
    staff: 'Staff',
    nurse: 'Nurse',
    med_tech: 'Med Tech',
    auditor: 'Auditor'
  }
  return labels[role] || role
})

const toggleSidebar = () => {
  isCollapsed.value = !isCollapsed.value
}

const handleLogout = () => {
  authStore.logout()
  router.push('/login')
}
